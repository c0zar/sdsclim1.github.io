document.addEventListener('DOMContentLoaded', () => {
    const langBtn = document.getElementById('langBtn');
    const langDropdown = document.getElementById('langDropdown');
    const currentLangText = document.getElementById('currentLang');
    const langOptions = document.querySelectorAll('.lang-option');

    // 1. Gestion de l'affichage du menu déroulant des langues
    langBtn.addEventListener('click', (e) => {
        e.stopPropagation();
        langDropdown.classList.toggle('show');
    });

    document.addEventListener('click', () => {
        langDropdown.classList.remove('show');
    });

    // 2. Fonction principale de traduction
    function changeLanguage(lang) {
        localStorage.setItem('sds_lang', lang);
        currentLangText.textContent = lang.toUpperCase();

        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            if (translations[lang] && translations[lang][key]) {
                element.textContent = translations[lang][key];
            }
        });
    }

    // 3. Écouteur de clic sur les options du menu de langues
    langOptions.forEach(option => {
        option.addEventListener('click', (e) => {
            const selectedLang = e.target.getAttribute('data-lang');
            changeLanguage(selectedLang);
        });
    });

    // 4. Initialisation de la langue par défaut (FR) ou celle déjà choisie par le client
    const savedLang = localStorage.getItem('sds_lang') || 'fr';
    changeLanguage(savedLang);
});